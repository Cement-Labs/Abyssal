import Foundation

extension Int8 {
    
    var cgFloat: CGFloat {
        return CGFloat(self)
    }
    
    var float16: Float16 {
        return Float16(self)
    }
    
}

extension Int32 {
    
    var cgFloat: CGFloat {
        return CGFloat(self)
    }
    
    var float16: Float16 {
        return Float16(self)
    }
    
}
